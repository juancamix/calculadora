let n1 = 1;
let n2 = 2;

function sum(n1, n2) {
  result = n1 + n2 
  return `suma: ${result}` 
}
console.log(sum(n1,n2))

function min (n1, n2){
result = n1 - n2 
return `resta: ${result}`
}
console.log(min(n1,n2))

function multi(n1, n2) {
  result = n1 * n2
  return `multi: ${result}`
}
console.log(multi(n1,n2))

function div(n1, n2) {
  result = n1 / n2
  return `division: ${result}`
}
console.log(div(n1,n2))

function mod(n1, n2) {
  result = n1 % n2 
  return `modulo: ${result}`
}
console.log(mod(n1,n2));